package net.jw.meeting.model;

public class BookingUsers {
	
	
	private int bUserId;
	private String bUserName;
	private String bUserEmail;
	private String bUserMobile;
	
	
	public BookingUsers(){

		
		
	}
	
	
	
	public int getbUserId() {
		return bUserId;
	}
	public void setbUserId(int bUserId) {
		this.bUserId = bUserId;
	}



	public String getbUserName() {
		return bUserName;
	}



	public void setbUserName(String bUserName) {
		this.bUserName = bUserName;
	}



	public String getbUserEmail() {
		return bUserEmail;
	}



	public void setbUserEmail(String bUserEmail) {
		this.bUserEmail = bUserEmail;
	}



	public String getbUserMobile() {
		return bUserMobile;
	}



	public void setbUserMobile(String bUserMobile) {
		this.bUserMobile = bUserMobile;
	}
	

}
