package net.jw.meeting.model;

public class CommonDetails {
	
	private String status;
	private int createdById;
	private String createdByTime;
	private int updatedById;
	private String updatedByTime;
	
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getCreatedById() {
		return createdById;
	}
	public void setCreatedById(int createdById) {
		this.createdById = createdById;
	}
	public String getCreatedByTime() {
		return createdByTime;
	}
	public void setCreatedByTime(String createdByTime) {
		this.createdByTime = createdByTime;
	}
	public int getUpdatedById() {
		return updatedById;
	}
	public void setUpdatedById(int updatedById) {
		this.updatedById = updatedById;
	}
	public String getUpdatedByTime() {
		return updatedByTime;
	}
	public void setUpdatedByTime(String updatedByTime) {
		this.updatedByTime = updatedByTime;
	}
	
	
	
}
